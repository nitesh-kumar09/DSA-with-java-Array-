
 class ArrayInitialize {
    
    public static void main(String[] args)
    {
        String[] fruits = new String[4];
        fruits[0] = "Apple";
        fruits[1] = "Orange";
        fruits[2] = "Banana";
        fruits[3] = "Pineapple";

        for(String items:fruits)
        {
            System.out.println(items);
        }

    }

}
